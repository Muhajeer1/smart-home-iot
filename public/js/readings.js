let allReadings = [];

document.addEventListener("DOMContentLoaded", () => {
  const simulatorPanel = document.getElementById("simulatorPanel");
  const simulatorHint = document.getElementById("simulatorHint");
  const simulateReadingBtn = document.getElementById("simulateReadingBtn");
  const readingMessage = document.getElementById("readingMessage");
  const selectedSensor = document.getElementById("selectedSensor");
  const readingFilter = document.getElementById("readingFilter");

  const sensor_id = localStorage.getItem("sensor_id");
  const sensor_type = localStorage.getItem("sensor_type");
  const roleId = localStorage.getItem("role_id");
  const canSimulateReadings = roleId === "1" || roleId === "3";

  if (!sensor_id) {
    setMessage(readingMessage, "No sensor selected.", "error");
    return;
  }

  selectedSensor.textContent = `Selected Sensor: ${formatLabel(sensor_type)}`;

  loadReadings();

  readingFilter.addEventListener("input", () => {
    renderReadings(allReadings, readingFilter.value);
  });

  setupToggleButton(
    "toggleReadingList",
    "readingList",
    "Hide Readings",
    "Show Readings",
  );

  if (!canSimulateReadings) {
    simulateReadingBtn.hidden = true;
    simulatorPanel.classList.add("read-only-panel");
    simulatorHint.textContent =
      "Readings are generated by the IoT simulator. Home users can view sensor history only.";
    return;
  }

  simulateReadingBtn.addEventListener("click", async () => {
    try {
      simulateReadingBtn.disabled = true;
      setMessage(readingMessage, "Generating simulated reading...", "warning");

      const response = await fetch("/readings/simulate", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({ sensor_id }),
      });

      const result = await response.json();

      if (response.ok) {
        setMessage(readingMessage, "Simulated reading generated successfully.", "good");
        loadReadings();
      } else {
        setMessage(
          readingMessage,
          result.error || "Failed to generate simulated reading.",
          "error",
        );
      }
    } catch (err) {
      console.error(err);
      setMessage(readingMessage, "Server error.", "error");
    } finally {
      simulateReadingBtn.disabled = false;
    }
  });
});

async function loadReadings() {
  const sensor_id = localStorage.getItem("sensor_id");
  const readingFilter = document.getElementById("readingFilter");

  try {
    const response = await fetch(`/readings/${sensor_id}`);
    allReadings = await response.json();
    renderReadings(allReadings, readingFilter.value);
  } catch (err) {
    console.error("Error loading readings:", err);
  }
}

function renderReadings(readings, filterText = "") {
  const readingList = document.getElementById("readingList");
  readingList.innerHTML = "";

  const filteredReadings = readings.filter((reading) => {
    const quality = (reading.quality_flag || "").toLowerCase();
    return quality.includes(filterText.toLowerCase());
  });

  filteredReadings.forEach((reading) => {
    const li = document.createElement("li");
    li.className = "list-row";

    const time = new Date(reading.reading_time).toLocaleString();
    const quality = (reading.quality_flag || "").toLowerCase();

    li.appendChild(
      createDetailList([
        { label: "Time", value: time, primary: true },
        { label: "Value", value: reading.reading_value },
        {
          label: "Quality",
          value: formatLabel(reading.quality_flag),
          tone: getTone(quality),
        },
      ]),
    );

    readingList.appendChild(li);
  });
}
